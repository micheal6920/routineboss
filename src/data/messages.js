// Dynamic message generator
export const getMotivationalMessage = (category = 'general', mode = 'roast', taskTitle = '') => {
  const messages = mode === 'roast'
    ? (FUNNY_MESSAGES[category] || FUNNY_MESSAGES.general)
    : (FRIENDLY_MESSAGES[category] || FRIENDLY_MESSAGES.general);

  // Add task-specific roast if title has keywords
  const taskLower = taskTitle.toLowerCase();
  let extraMessages = [];

  if (mode === 'roast') {
    if (taskLower.includes('gym') || taskLower.includes('workout')) extraMessages = FUNNY_MESSAGES.fitness;
    if (taskLower.includes('code') || taskLower.includes('bug') || taskLower.includes('deploy')) extraMessages = FUNNY_MESSAGES.coding;
    if (taskLower.includes('study') || taskLower.includes('exam') || taskLower.includes('read')) extraMessages = FUNNY_MESSAGES.study;
    if (taskLower.includes('sleep') || taskLower.includes('bed') || taskLower.includes('rest')) extraMessages = FUNNY_MESSAGES.sleep;
  }

  const pool = [...messages, ...extraMessages];
  return pool[Math.floor(Math.random() * pool.length)];
};

